@dataclass(frozen=True)
class FakeEntry:
    """One filesystem entry as ``ls -l`` would render it."""

    name: str
    mode: int
    size: int
    owner: str = "root"
    group: str = "root"
    mtime: int = 1715000000  # frozen per session, plausible recent epoch
    is_dir: bool = False
    is_symlink: bool = False
    target: str | None = None


@dataclass(frozen=True)
class ReadResult:
    """Outcome of a :func:`read` call."""

    status: Literal["content", "permission_denied", "not_in_fakefs"]
    content: str = ""


@dataclass(frozen=True)
class ListResult:
    """Outcome of a :func:`listdir` call."""

    status: Literal["entries", "not_in_fakefs"]
    entries: tuple[FakeEntry, ...] = field(default_factory=tuple)


