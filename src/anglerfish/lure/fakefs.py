def _format_template(template: str, *, user: str, hostname: str) -> str:
    """Render the {user} and {hostname} placeholders inside a template body.

    Uses ``str.replace`` rather than ``str.format`` so shell-syntax
    braces (``${VAR-default}``, awk programs, JSON in motd) survive
    intact. Only the two literal placeholders we ship are substituted.
    """
    return template.replace("{user}", user).replace("{hostname}", hostname)


def _format_entries(
    entries: tuple[FakeEntry, ...],
    *,
    user: str,
) -> tuple[FakeEntry, ...]:
    """Expand ``{user}`` placeholders in entry names / owners."""
    return tuple(
        FakeEntry(
            name=e.name.replace("{user}", user),
            mode=e.mode,
            size=e.size,
            owner=e.owner.replace("{user}", user),
            group=e.group.replace("{user}", user),
            mtime=e.mtime,
            is_dir=e.is_dir,
            is_symlink=e.is_symlink,
            target=e.target,
        )
        for e in entries
    )


def _resolve_user_path(path: str, *, user: str) -> str:
    """Substitute ``{user}`` into a static path, if present."""
    return path.replace("{user}", user)


def read(path: str, session: LureSessionContext) -> ReadResult:
    """Return the contents of ``path`` from the static filesystem.

    ``status`` distinguishes three cases:

    - ``content``: path is in the table; return the formatted body.
    - ``permission_denied``: path exists conceptually but is unreadable.
      The caller should synthesise the canonical permission-denied
      error in the shell.
    - ``not_in_fakefs``: path is unknown; the caller should route to
      the bridge.
    """
    if path in _PERMISSION_DENIED:
        return ReadResult(status="permission_denied")

    # Template paths keyed under "/home/{user}/..." live in the
    # _FILES dict with the literal placeholder. Look up both raw
    # path and its template-rendered key.
    user = session.username
    hostname = session.hostname

    if path in _FILES:
        template, _mode, _owner, _group = _FILES[path]
        return ReadResult(
            status="content",
            content=_format_template(template, user=user, hostname=hostname),
        )

    # Convert the live path back to a template ("/home/alice/.bashrc"
    # -> "/home/{user}/.bashrc") and try again.
    template_path = path.replace(f"/home/{user}", "/home/{user}")
    if template_path != path and template_path in _FILES:
        template, _mode, _owner, _group = _FILES[template_path]
        return ReadResult(
            status="content",
            content=_format_template(template, user=user, hostname=hostname),
        )

    return ReadResult(status="not_in_fakefs")


def listdir(path: str, session: LureSessionContext) -> ListResult:
    """Return the entries inside ``path`` from the static filesystem."""
    user = session.username
    normalised = path.rstrip("/") or "/"

    if normalised == "/":
        return ListResult(status="entries", entries=_ROOT_DIR_ENTRIES)
    if normalised == "/etc":
        return ListResult(status="entries", entries=_ETC_DIR_ENTRIES)
    if normalised == "/var":
        return ListResult(status="entries", entries=_VAR_DIR_ENTRIES)
    if normalised == "/proc":
        return ListResult(status="entries", entries=_PROC_DIR_ENTRIES)
    if normalised == "/home":
        return ListResult(
            status="entries",
            entries=(FakeEntry(user, _D755, 4096, owner=user, group=user, is_dir=True),),
        )
    if normalised == f"/home/{user}":
        return ListResult(
            status="entries",
            entries=_format_entries(_HOME_DIR_ENTRIES_TEMPLATE, user=user),
        )
    if normalised == "/root":
        return ListResult(status="entries", entries=_ROOT_HOME_ENTRIES)
    if normalised in (f"/home/{user}/.ssh", "/root/.ssh"):
        return ListResult(status="entries", entries=_SSH_DIR_ENTRIES)
    if normalised == "/var/log":
        return ListResult(status="entries", entries=_VAR_LOG_DIR_ENTRIES)

    return ListResult(status="not_in_fakefs")


