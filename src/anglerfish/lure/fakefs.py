"""Static in-process filesystem the lure serves natively.

About 50 paths covering the standard recon targets a botnet hits in
the first minutes of a Linux foothold: system identity, system config,
kernel/hardware, user homes, common logs, top-level listings. Anything
outside this table routes to the bridge.

The set is intentionally curated for the *known* attacker-cat paths
(sudoers, crontab, passwd, shadow, ssh configs, recent log lines) so
the LLM never needs to invent file contents that would contradict
what the lure already serves.

Content is identical across sessions and across the lifetime of the
lure process: the design (see ``docs/design/STAGE_2_lure_subsystem.md``
"Static fake filesystem") deliberately rejects per-session randomness
for v1 to bound complexity and avoid contradictions between bridge-
served and lure-served output.
"""

from __future__ import annotations

